    private void btnCATActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCATActionPerformed
        Categreps.aSel = true;
        new Categreps(null,true).setVisible(true);
        try {
            Rep.setCategoria(new Categrep(Categreps.ID));
            txtCAT.setText(Rep.getCategoria().getNombre());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error al asignar categoría", JOptionPane.ERROR_MESSAGE);
            dispose();
        }
    }//GEN-LAST:event_btnCATActionPerformed

