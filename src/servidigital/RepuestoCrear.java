    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        if(txtCAT.getText().length() > 0 && txtCodigo.getText().length() <= 45 && txtNombre.getText().length() <= 45 && 200 >= txtDescrip.getText().length() && txtCodigo.getText().length() >= 5 && txtNombre.getText().length() >= 10 && txtDescrip.getText().length() >= 15) {
            try {
                Rep.Registrar(txtCodigo.getText(), Rep.getCategoria().getID(), txtNombre.getText(), txtDescrip.getText());
            }
            catch(SQLException e) {
                JOptionPane.showMessageDialog(this, Main.BD.showException(e.getMessage()), "Diagnostico.registrar(String)", JOptionPane.ERROR_MESSAGE);
            }
            dispose();
        }
        else {
            JOptionPane.showMessageDialog(null, "Limitantes: \n Codigo: 05 - 45 caracteres\n Nombre: 10 - 45 caracteres\n Descripcion: 15 - 200 caracteres", "Atención", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnOKActionPerformed

