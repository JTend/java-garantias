    private boolean rifnomVal() {
        try {
            if(D.empresaValida() || (D.getRIF().equals(txtRif.getText()) && D.getNombre().equals(txtNombre.getText())))
            return true;
        else
            return false;
        }
        catch(SQLException e) {
            JOptionPane.showMessageDialog(this, Main.BD.showException(e.getMessage()), "rifnomVal()", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

    private boolean datosValidos() {
        if(rifnomVal() && txtRif.getText().length() >= 15 && txtNombre.getText().length() >= 10 && txtTelefono.getText().length() >= 7 && txtDireccion.getText().length() >= 10 && txtEmail.getText().length() >= 8 && txtWebsite.getText().length() >= 10)
            return true;
        else
            return false;
    }

