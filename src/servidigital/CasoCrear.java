    private boolean verificarCedula() {
        try {
            if(txtCedula.getText().length() > 0) {
                Main.BD.Consultar("select nombre from clientes where cedula = " + txtCedula.getText());
                if(Main.BD.Siguiente()) {
                    txtNombre.setText(Main.BD.Campo("nombre"));
                    return true;
                }
                else {
                    txtNombre.setText("");
                    return false;
                }
            }
            else return false;
        }
        catch(SQLException e) {
            JOptionPane.showMessageDialog(this, Main.BD.showException(e.getMessage()), "Error verificarCedula()", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

    private void cargarTiendas() {
        DefaultComboBoxModel cmb = new DefaultComboBoxModel();
        try {
            Main.BD.Consultar("select nombre from tiendas");
            while(Main.BD.rslSet().next()) {
                cmb.addElement(Main.BD.rslSet().getString("nombre"));
            }
        }
        catch(SQLException Ex) {
            JOptionPane.showMessageDialog(this, Ex.getMessage());
        }
        lstTiendas.setModel(cmb);
        try {
            T.cargarTienda(lstTiendas.getSelectedItem().toString());
        }
        catch(SQLException Ex) {
            JOptionPane.showMessageDialog(this, Ex.getMessage());
        }
    }

    private void validarEquipo() {
        try {
            Main.BD.Ejecutar("start transaction");
            if(txtCodigo.getText().length() >= 10) {
                Main.BD.Consultar("select descripcion from productos where codigo = '"+ txtCodigo.getText() +"'");
                if( ! Main.BD.Siguiente()) {
                    Main.Saint.Consultar("select codprod, descrip, refere from saprod where codprod = '"+ txtCodigo.getText() +"'");
                    if(Main.Saint.Siguiente()) {
                        new selMarca(null, true).setVisible(true);
                        Main.BD.Ejecutar("insert into productos (id_marca, codigo, descripcion, referencia) values ("+selMarca.id_marca+",'"+Main.Saint.Campo("codprod")+"','"+Main.Saint.Campo("descrip")+"','"+Main.Saint.Campo("refere")+"')");
                        txtDesc_equipo.setText(Main.Saint.Campo("descrip"));
                    }
                    else {
                        txtDesc_equipo.setText("");
                    }
                }
                else {
                    txtDesc_equipo.setText(Main.BD.Campo("descripcion"));
                }
                lblEquipo.setVisible(true);
            }
            else
                lblEquipo.setVisible(false);
            Main.BD.Ejecutar("commit");
        }
        catch(SQLException e) {
            JOptionPane.showMessageDialog(this, Main.BD.showException(e.getMessage()), "Error validarEquipo()", JOptionPane.ERROR_MESSAGE);
            Main.BD.Ejecutar2("rollback");
        }
    }

