    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            Main.BD.Ejecutar("start transaction");
            E.registrar(txtCodigo.getText(), txtSerial.getText());
            C.registrar(txtCedula.getText(), T.getID(), E.getID(), txtFactura.getText(), txtDesc_caso.getText());
            this.setVisible(false);
            C.imprimirApertura();
            Main.BD.Ejecutar("commit");
            dispose();
        }
        catch(SQLException ee) {
            JOptionPane.showMessageDialog(this, Main.BD.showException(ee.getMessage()), "Error al crear caso", JOptionPane.ERROR_MESSAGE);
            Main.BD.Ejecutar2("rollback");
        }
}//GEN-LAST:event_btnGuardarActionPerformed

