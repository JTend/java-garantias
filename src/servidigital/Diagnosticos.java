    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        if(tblDiagnosticos.getSelectedRow() >= 0) {
            ID = tblDiagnosticos.getValueAt(tblDiagnosticos.getSelectedRow(), 0).toString();
            if(aSel) {
                aSel = false;
                dispose();
            }
            else {
                new DiagnosticoEditar(null,true).setVisible(true);
            }
        }
        else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un registro de la tabla", "Atenci√≥n", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnOKActionPerformed

