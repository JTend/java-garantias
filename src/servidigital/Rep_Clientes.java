    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        int I;
        int F;
        try {
            I = Integer.parseInt(txtDesde.getText());
            F = Integer.parseInt(txtHasta.getText());
            Map P = new HashMap();
            P.put("desde", I);
            P.put("hasta", F);
            new Reporte("/Reportes/clientes1.jasper",P).runReporte("Clientes", true);
            this.dispose();
        }
        catch(java.lang.NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Alguno de los valores ingresados no es un numero válido", e.getMessage(), JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnOKActionPerformed

