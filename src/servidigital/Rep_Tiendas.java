    private DefaultListModel cargarTiendas() {
        DefaultListModel cmb = new DefaultListModel();
        try {
            Main.BD.Consultar("select nombre from tiendas");
            while(Main.BD.rslSet().next()) {
                cmb.addElement(Main.BD.rslSet().getString("nombre"));
            }
        }
        catch(SQLException Ex) {
            JOptionPane.showMessageDialog(this, Ex.getMessage());
        }
        return cmb;
    }
