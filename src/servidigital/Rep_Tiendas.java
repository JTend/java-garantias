    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        String Ti = ".";
        if(Seleccionados.getSize() > 0) {
            for(int i = 0; i < Seleccionados.getSize(); i++) {
                Ti = Ti + Seleccionados.getElementAt(i).toString() + ".";
            }
            if(this.chckGlobal.isSelected()) Reporte = "/Reportes/tiendasGlobal.jasper";
            if(this.chckMarcas.isSelected()) Reporte = "/Reportes/tiendasMarcas.jasper";
            if(this.chckEquipos.isSelected()) Reporte = "/Reportes/tiendasEquipos.jasper";
            if(this.chckClientes.isSelected()) Reporte = "/Reportes/tiendasClientes.jasper";
            if(this.chckDiagnosticos.isSelected()) Reporte = "/Reportes/tiendasDiagnosticos.jasper";
            Map P = new HashMap();
            P.put("desde", dcDesde.getSelectedDate().getTime());
            P.put("hasta", dcHasta.getSelectedDate().getTime());
            P.put("ubicaciones", Ti);
            new Reporte(Reporte,P).runReporte("Casos por tiendas", true);
        }
        else JOptionPane.showMessageDialog(null, "Debe seleccionar al menos una tienda para este reporte");
    }//GEN-LAST:event_btnOKActionPerformed

