    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        try {
            if(tblDatos.isEnabled()) {
                new Deposito(txtCodigo.getText(), txtDescripcion.getText()).Registrar();
            }
            else {
                Deposito depo = new Deposito(Integer.parseInt(tblDatos.getValueAt(tblDatos.getSelectedRow(), 0).toString()));
                depo.setCodigo(txtCodigo.getText());
                depo.setDescripcion(txtDescripcion.getText());
                depo.Actualizar();
            }
            limpiarControles();
            desactivarControles();
            tblDatos.setEnabled(true);
        }
        catch(SQLException e) {
            limpiarControles();
            desactivarControles();
            tblDatos.setEnabled(true);
            JOptionPane.showMessageDialog(this, Main.BD.showException(e.getMessage()), "Depositos - Registrar()", JOptionPane.ERROR_MESSAGE);
        }
        cargarDepositos();
    }//GEN-LAST:event_btnOKActionPerformed

