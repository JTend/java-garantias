    private void tblDatosKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblDatosKeyPressed
        if((evt.getKeyCode() == 127) && (tblDatos.getSelectedRow() >= 0)) {
            try {
                new Deposito(Integer.parseInt(tblDatos.getValueAt(tblDatos.getSelectedRow(), 0).toString())).Suprimir();
                JOptionPane.showMessageDialog(this, "Deposito borrado: " + tblDatos.getValueAt(tblDatos.getSelectedRow(), 1).toString());
                cargarDepositos();
            }
            catch(SQLException e) {
                JOptionPane.showMessageDialog(this, Main.BD.showException(e.getMessage()), "Depositos - Borrar()", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_tblDatosKeyPressed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if(tblDatos.getSelectedRow() >= 0) {
            tblDatos.setEnabled(false);
            activarControles();
            txtCodigo.requestFocus();
        }
        else {
            tblDatos.setEnabled(true);
            desactivarControles();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void tblDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDatosMouseClicked
        try {
            Deposito depo = new Deposito(Integer.parseInt(tblDatos.getValueAt(tblDatos.getSelectedRow(), 0).toString()));
            txtCodigo.setText(depo.getCodigo());
            txtDescripcion.setText(depo.getDescripcion());
        }
        catch(SQLException e) {
            JOptionPane.showMessageDialog(this, Main.BD.showException(e.getMessage()), "Depositos - Constructor(1)", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tblDatosMouseClicked

