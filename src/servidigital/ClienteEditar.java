    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            if(cedulaValida() && correoValido() && direccionValida() && datosCambiaron()) {
                editarCliente();
                this.dispose();
            }
            else
                JOptionPane.showMessageDialog(this, "Aún restan datos por ingresar,\nmodificar o bien los que se han ingresado\nno son correctos", "Datos incompletos!", JOptionPane.ERROR_MESSAGE);
        }
        catch(SQLException e) {
            
        }
}//GEN-LAST:event_btnGuardarActionPerformed

