    private void cargarCliente() throws SQLException {
        txtNombre.setText(Inicial.getNombre());
        txtCedula.setText(Inicial.getCedula());
        txtEmail.setText(Inicial.getEmail());
        txtDireccion.setText(Inicial.getDireccion());

        Main.BD.Consultar("select * from telefonos where cedula = " + Inicial.getCedula());
        T = Inicial.buscarTelefonos();
        lstTelefonos.setModel(T);
    }

    private void agregarTelefono() {
        if(txtTelefono.getText().length() > 6) {
            T.addElement(txtTelefono.getText());
            txtTelefono.setText("");
        }
        telefonosCambiaron = true;
        validarTelefonos();
    }
    private void quitarTelefono() {
        if(T.getSize() > 0)
            T.removeElementAt(lstTelefonos.getSelectedIndex());
        telefonosCambiaron = true;
        validarTelefonos();
    }

    private void validarTelefonos() {
        lblTelefonos.setVisible(telefonosValidos());
    }

    private boolean telefonosValidos() {
        return lstTelefonos.getModel().getSize() > 0;
    }

    private boolean cedulaValida() throws SQLException {
        if(txtCedula.getText().equals(Inicial.getCedula())) return true;
        if(txtCedula.getText().length() < 3) return false;
        Main.BD.Consultar("select * from clientes where cedula = " + txtCedula.getText());
        return !Main.BD.rslSet().next();
    }

    private boolean datosCambiaron() {
        return telefonosCambiaron || !txtCedula.getText().equals(Inicial.getCedula()) || !txtNombre.getText().equals(Inicial.getNombre()) || !txtEmail.getText().equals(Inicial.getEmail()) || !txtDireccion.getText().equals(Inicial.getDireccion());
    }

    private boolean correoValido() {
        txtEmail.setText(Cadenas.sinEspacios(100,txtEmail.getText()));
        return Cadenas.correoValido(txtEmail.getText());
    }

    private void editarCliente() throws SQLException {
            Inicial.registrarTelefonos(lstTelefonos.getModel());
            Inicial.actualizar(txtCedula.getText(), txtNombre.getText(), txtEmail.getText(), txtDireccion.getText(), Boolean.toString(Inicial.getWeb()));

    }

    private void eliminarCliente() {
        try {
            Inicial.suprimir();
        }
        catch(SQLException e) {
            JOptionPane.showMessageDialog(this, Main.BD.showException(e.getMessage()), "eliminarCliente()", JOptionPane.ERROR_MESSAGE);
        }
    }

    private boolean direccionValida() {
        return Cadenas.MiTrim(150, txtDireccion.getText()).length() > 4;
    }
