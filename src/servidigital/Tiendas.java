    private void cargarTiendas() {
        try {
            Main.BD.Consultar("select id_tienda, nombre, telefono from tiendas where instr(id_tienda, '"+ txtBuscar.getText() +"') or instr(nombre, '"+ txtBuscar.getText() +"')");
            tblTiendas.setModel(Main.BD.Modelo());
        }
        catch(SQLException e) {
            JOptionPane.showMessageDialog(this, Main.BD.showException(e.getMessage()), "cargarTiendas()", JOptionPane.ERROR_MESSAGE);
        }
    }

