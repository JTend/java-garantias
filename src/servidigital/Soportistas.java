    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        if(tblSoportistas.getSelectedRow() >= 0) {
            ID = tblSoportistas.getValueAt(tblSoportistas.getSelectedRow(), 0).toString();
            if(aSel) {
                aSel = false;
                dispose();
            } else {
                new SoportistaEditar(null,true).setVisible(true);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un registro de la tabla", "Atenci√≥n", JOptionPane.ERROR_MESSAGE);
        }
}//GEN-LAST:event_btnOKActionPerformed

